    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //update button
        int eid=Integer.parseInt(id.getText());
        String name=n.getText();
        java.util.Date dt=d.getDate();
        String address=a.getText();
        if(dt!=null  &&  !n.equals("") &&  !a.equals("")){
            String gender;
            if(g1.isSelected())
            gender=g1.getText();
            else if(g2.isSelected())
            gender=g2.getText();
            else
            gender=g3.getText();
            String country=(String)c.getSelectedItem();
            String language="";
            if(l1.isSelected())
            language +=l1.getText()+",";
            if(l2.isSelected())
            language +=l2.getText()+",";
            if(l3.isSelected())
            language +=l3.getText()+",";
            
            language=language.substring(0, language.length()-1);

            try {
               
                Dbconnect.Dbc.updateUser.setString(1, name);
                Dbconnect.Dbc.updateUser.setString(2, gender);
                //code to convert java.util.Date to java.sql.date
                java.sql.Date dob=new java.sql.Date(dt.getTime());
                Dbconnect.Dbc.updateUser.setDate(3, dob);
                Dbconnect.Dbc.updateUser.setString(4, country);
                Dbconnect.Dbc.updateUser.setString(5, address);
                Dbconnect.Dbc.updateUser.setString(6, language);
                Dbconnect.Dbc.updateUser.setInt(7, eid);
               
                Dbconnect.Dbc.updateUser.executeUpdate();

                tKeyReleased(null);
                JOptionPane.showMessageDialog(null,
                    "Data Updated!");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null,
                "Plz Fill the complete Form!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

