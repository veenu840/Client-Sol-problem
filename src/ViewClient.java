    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
      int ri=jTable.getSelectedRow();
      if(ri!=-1){
    int eid=(Integer)jTable.getValueAt(ri, 0);
    String name=(String)jTable.getValueAt(ri,1);
    String gender=(String)jTable.getValueAt(ri,2);
    java.util.Date dob=(java.util.Date)jTable.getValueAt(ri,3);
    String country=(String)jTable.getValueAt(ri,4);
    String address=(String)jTable.getValueAt(ri,5);
    String language=(String)jTable.getValueAt(ri,6);
    id.setText(eid+"");
    n.setText(name);
    if(gender.equals("Male"))
        g1.setSelected(true);
    else if(gender.equals("Female"))
        g2.setSelected(true);
    else 
        g3.setSelected(true);
    d.setDate(dob);
    c.setSelectedItem(country);
    a.setText(address);
    l1.setSelected(false);
    l2.setSelected(false);
    l3.setSelected(false);
    
    java.util.StringTokenizer st=
            new java.util.StringTokenizer(language,",");
    while(st.hasMoreTokens()){
        String l=st.nextToken();
        if(l.equals("Hindi"))
            l1.setSelected(true);
        else if(l.equals("English"))
            l2.setSelected(true);
        else
            l3.setSelected(true);
        
    }
        }      
        
    }//GEN-LAST:event_jTableMouseClicked

